ADRESSA EQU 0640H
ADRESSB EQU 0642H
ADRESSCT EQU 0646H
CODE SEGMENT
	ASSUME CS:CODE
START:
MOV DX,ADRESSCT
MOV AL,80H; 10000000B 方式0 A口输出 C口高4位输出 方式0 B口输出 C口低4位输出
OUT DX,AL
MOV BX,8001H
AA1:
MOV DX,ADRESSA
MOV AL,BH
OUT DX,AL
ROR BH,1;循环右移
MOV DX,ADRESSB
MOV AL,BL
OUT DX,AL
ROL BL,1;循环左移
CALL DELAY
CALL DELAY
JMP AA1
DELAY:
PUSH CX
MOV CX,0F000H
AA2:
PUSH AX
POP AX
LOOP AA2
POP CX
RET
CODE ENDS
END START

